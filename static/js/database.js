$(function() {
    $('#allowD').click(function() {
 
        $.ajax({
            url: '/dashboards/',
            data: $('form').serialize(),
            type: 'POST',
            success: function(response) {
                console.log(response);
            },
            error: function(error) {
                console.log(error);
            }
        });
    });
});

